/**
 * Calculate binary CRC32 for the given string
 * Equivalent to dacom.GenCRC32 in Freelancer
 */
export function flCrc32Binary(str: string): number {
  let crc = 0xffffffff;

  for (let i = 0; i < str.length; i++) {
    const charCode = str.charCodeAt(i);
    crc = (crc >>> 8) ^ flcrc32tbl[(crc ^ charCode) & 0xff];
  }

  return ~crc >>> 0; // Unsigned bitwise NOT (to match C++ behavior)
}

/**
 * Calculate case-insensitive CRC32 for the given string
 * Equivalent to dacom.GenCRC32 with case-insensitivity in Freelancer
 */
export function flCrc32(str: string): number {
  let crc = 0xffffffff;

  for (let i = 0; i < str.length; i++) {
    const charCode = str.toLowerCase().charCodeAt(i);
    crc = (crc >>> 8) ^ flcrc32tbl[(crc ^ charCode) & 0xff];
  }

  return ~crc >>> 0; // Unsigned bitwise NOT (to match C++ behavior)
}

