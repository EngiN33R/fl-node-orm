/**
 * Create a hash ID for the given string
 * Equivalent to common.CreateID in Freelancer
 */
export function createHashId(str: string) {
  let hash = 0;

  for (let i = 0; i < str.length; i++) {
    const charCode = str.toLowerCase().charCodeAt(i);
    hash = (hash >>> 8) ^ flcrcm16tbl[(hash ^ charCode) & 0xff];
  }

  // Swap the bytes around
  hash =
    ((hash >>> 24) |
      ((hash >>> 8) & 0xff00) |
      ((hash & 0xff00) << 8) |
      (hash << 24)) >>>
    0;

  hash = ((hash >>> 2) | 0x80000000) >>> 0;

  return `0x${hash.toString(16).toUpperCase()}`;
}
